#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*c;
	size_t			a;

	a = 0;
	c = (unsigned char *)s;
	while (a < n)
	{
		c[a] = '\0';
		a++;
	}
}
