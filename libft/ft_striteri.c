#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	a;

	if (!s || !f)
		return ;
	a = 0;
	while (s[a])
	{
		(*f)(a, &s[a]);
		a++;
	}
}
